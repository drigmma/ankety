def append_row_sync(sheet_name: str, row: list[str]):
    """–î–æ–±–∞–≤–∏—Ç—å —Å—Ç—Ä–æ–∫—É –≤ —É–∫–∞–∑–∞–Ω–Ω—ã–π –ª–∏—Å—Ç (—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–∞—è –≤–µ—Ä—Å–∏—è)."""
