async def save_form_to_sheets(form_type: FormType, answers: list[str]):
    """–°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –∞–Ω–∫–µ—Ç—ã –≤ –æ–±—â–∏–π –ª–∏—Å—Ç –∏ –≤ –ø—Ä–æ—Ñ–∏–ª—å–Ω—ã–π –ª–∏—Å—Ç."""
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    form_name = human_form_name(form_type)

    row = [form_name, timestamp] + answers

    # –æ–±—â–∏–π –ª–∏—Å—Ç
    await append_row(SHEET_ALL, row)

    # –ø—Ä–æ—Ñ–∏–ª—å–Ω—ã–π –ª–∏—Å—Ç
    if form_type == FormType.PARENT_FULL:
        await append_row(SHEET_PARENT_FULL, row)
    elif form_type == FormType.PARENT_SHORT:
        await append_row(SHEET_PARENT_SHORT, row)
    elif form_type == FormType.CHILD_FULL:
        await append_row(SHEET_CHILD_FULL, row)
    elif form_type == FormType.CHILD_SHORT:
        await append_row(SHEET_CHILD_SHORT, row)


