async def send_question(message: Message, form_type: FormType, index: int):
    """–û—Ç–ø—Ä–∞–≤–∏—Ç—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –≤–æ–ø—Ä–æ—Å —Å –∫—Ä–∞—Å–∏–≤—ã–º –æ—Ñ–æ—Ä–º–ª–µ–Ω–∏–µ–º –∏, –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏, –∫–Ω–æ–ø–∫–∞–º–∏ –î–∞/–ù–µ—Ç."""
    questions = FORM_QUESTIONS[form_type]
    q_text = questions[index]
    total = len(questions)
    progress = f"–í–æ–ø—Ä–æ—Å {index + 1} –∏–∑ {total}"

    text = f"‚ú® <b>{progress}</b>\n\n{q_text}"

    if is_yes_no_question(q_text):
        await message.answer(
            text,
            reply_markup=yes_no_keyboard(),
            parse_mode="HTML",
        )
    else:
        await message.answer(
            text,
            parse_mode="HTML",
        )


